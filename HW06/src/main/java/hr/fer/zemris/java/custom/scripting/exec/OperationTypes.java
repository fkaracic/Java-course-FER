package hr.fer.zemris.java.custom.scripting.exec;

/**
 * Representation of operations. Supported operations are: ADDITION,
 * SUBTRACTION, DIVISION, MULTIPLICATION, COMPARISON.
 * 
 * @author Filip Karacic
 *
 */
public enum OperationTypes {

	/**
	 * Addition operator.
	 */
	ADD,

	/**
	 * Subtraction operator.
	 */
	SUB,

	/**
	 * Division operator.
	 */
	DIV,

	/**
	 * Multiplication operator.
	 */
	MUL,

	/**
	 * Comparison operator.
	 */
	COMP;
}